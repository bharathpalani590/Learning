package nestedcondition;

public class NestedSimpleif {
    public static void main(String[] args) {
        int x = 10;
        int y = 5;
        if(x>0)
        {
        	System.out.println("x is positive");
            if(y>0)
            {
        	  System.out.println("y is positive");
            }
        }
    }
}
        



